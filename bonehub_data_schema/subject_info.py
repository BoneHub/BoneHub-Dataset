from . import BoneLabelMap


def check_valid_label_value(func):
    def wrapper(self, label: str, value: int):
        if label not in BoneLabelMap.get_names_list():
            raise ValueError(f"Invalid label: {label}. Refer to 'bonehub_data_schema/labelmap.py' to see valid labels.")
        if value not in {-1, 0, 1, 2}:
            raise ValueError(
                f"Invalid value: {value}. Valid values are: -1 (not available - not sure), 0 (not available - for sure), 1 (available - from original source), 2 (available - generated by BoneHub)"
            )

        return func(self, label, value)

    return wrapper


class SubjectInfo(dict):
    """Data dict for a subject."""

    valid_keys = (
        "dataset_id",
        "subject_id",
        "source_subject_path",
        "age",
        "gender",
        "weight",
        "height",
        "bmi",
        "imaging_modality",
        "image",
        "segmentation",
        "mesh",
        "nurbs",
    )

    def __init__(
        self,
        **kwargs,
    ):
        """
        Initialize a SubjectInfo dict with required information and optional information.
        Args:
            **kwargs: Optional keyword arguments for additional subject information. Valid keys are:
                - dataset_id (int): Unique identifier for the dataset in BoneHub.
                - subject_id (int): Unique identifier for the subject within the dataset in BoneHub.
                - source_subject_path (str): path to the subject in the source dataset.
                - age (int): Age of the subject.
                - gender (str): Gender of the subject (male or female or other).
                - weight (float): Weight of the subject in kg.
                - height (float): Height of the subject in cm.
                - bmi (float): Body Mass Index of the subject.
                - imaging_modality (str): Imaging modality used (e.g., CT, MRI).
                - image (int): availability value for the image (1 if available, 0 if not available).
                - segmentation (dict): Dictionary mapping LabelMap labels to their availability values for segmentation.
                - mesh (dict): Dictionary mapping LabelMap labels to their availability values for mesh.
                - nurbs (dict): Dictionary mapping LabelMap labels to their availability values for NURBS.
        """
        super().__init__()

        if not set(kwargs.keys()).issubset(set(self.valid_keys)):
            raise ValueError(f"Invalid keys: {set(kwargs.keys()) - set(self.valid_keys)}. Valid keys are: {self.valid_keys}")

        if dataset_id := kwargs.get("dataset_id"):
            self["dataset_id"] = dataset_id
        if subject_id := kwargs.get("subject_id"):
            self["subject_id"] = subject_id
        if source_subject_path := kwargs.get("source_subject_path"):
            self["source_subject_path"] = source_subject_path
        if age := kwargs.get("age"):
            self["age"] = age
        if gender := kwargs.get("gender"):
            self["gender"] = gender
        if weight := kwargs.get("weight"):
            self["weight"] = weight
        if height := kwargs.get("height"):
            self["height"] = height
        if bmi := kwargs.get("bmi"):
            self["bmi"] = bmi
        if imaging_modality := kwargs.get("imaging_modality"):
            self["imaging_modality"] = imaging_modality
        image = kwargs.get("image")
        if image is not None:
            self["image"] = image
        else:
            raise ValueError("Image availability must be provided (1 if available, 0 if not available).")

    def __setitem__(self, key, value):
        if key not in self.valid_keys:
            raise KeyError(f"Invalid key: {key}. Valid keys are: {self.valid_keys}")
        if key in {"dataset_id", "subject_id"} and not isinstance(value, int):
            raise ValueError(f"{key} must be an integer. Got {type(value)} instead.")
        if key == "source_subject_path":
            value = str(value).replace("\\", "/")  # Ensure consistent path format
        if key == "age" and not isinstance(value, int):
            raise ValueError(f"Age must be an integer. Got {type(value)} instead.")
        if key in {"weight", "height", "bmi"} and not isinstance(value, (int, float)):
            raise ValueError(f"{key} must be a number (int or float). Got {type(value)} instead.")
        if key == "image" and value not in {0, 1}:
            raise ValueError(f"Image availability must be 0 (not available) or 1 (available). Got {value} instead.")
        if key in {"segmentation", "mesh", "nurbs"}:
            raise KeyError(f"{key} should be set using the set_{key}_value method for each label, not directly.")
        super().__setitem__(key, value)

    def sorted(self) -> dict:
        """Return the keys in the SubjectInfo dict sorted according to valid_keys order."""
        sorted_dict = {key: self[key] for key in self.valid_keys if key in self}
        return sorted_dict

    @check_valid_label_value
    def set_segmentation_value(self, label: str, value: int):
        if "segmentation" not in self:
            super().__setitem__("segmentation", {})
        self["segmentation"][label] = value

    @check_valid_label_value
    def set_mesh_value(self, label: str, value: int):
        if "mesh" not in self:
            super().__setitem__("mesh", {})
        self["mesh"][label] = value

    @check_valid_label_value
    def set_nurbs_value(self, label: str, value: int):
        if "nurbs" not in self:
            super().__setitem__("nurbs", {})
        self["nurbs"][label] = value
