from . import BoneLabelMap


def check_valid_label_value(func):
    def wrapper(self, label: BoneLabelMap, value: int):
        if label not in BoneLabelMap:
            raise ValueError(f"Invalid label: {label}. Valid labels are: {[l.name for l in BoneLabelMap]}")
        if value not in {-1, 0, 1, 2}:
            raise ValueError(
                f"Invalid value: {value}. Valid values are: -1 (not available - not sure), 0 (not available - for sure), 1 (available - from original source), 2 (available - generated by BoneHub)"
            )

        return func(self, label, value)

    return wrapper


class SubjectInfo(dict):
    """Data dict for a subject."""

    valid_keys = {
        "dataset_id",
        "subject_id",
        "subject_id_source",
        "age",
        "gender",
        "imaging_modality",
        "imaging_manufacturer",
        "subject_orientation",
        "segmentation",
        "mesh",
        "nurbs",
    }

    def __init__(
        self,
        **kwargs,
    ):
        """
        Initialize a SubjectInfo dict with required information and optional information.
        Args:
            **kwargs: Optional keyword arguments for additional subject information. Valid keys are:
                - dataset_id (int): Unique identifier for the dataset in BoneHub.
                - subject_id (int): Unique identifier for the subject within the dataset in BoneHub.
                - subject_id_source (str): Subject ID from the original source.
                - age (int): Age of the subject
                - gender (str): Gender of the subject (male or female)
                - imaging_modality (str): Imaging modality used (e.g., CT, MRI)
                - imaging_manufacturer (str): Manufacturer of the imaging device (e.g., Siemens, GE, Philips)
                - subject_orientation (str): Orientation of the subject in the image (e.g., supine, prone, left lateral, right lateral)
                - segmentation (dict): Dictionary mapping LabelMap labels to their availability values for segmentation.
                - mesh (dict): Dictionary mapping LabelMap labels to their availability values for mesh.
                - nurbs (dict): Dictionary mapping LabelMap labels to their availability values for NURBS.
        """
        super().__init__()

        if not set(kwargs.keys()).issubset(self.valid_keys):
            raise ValueError(f"Invalid keys: {set(kwargs.keys()) - self.valid_keys}. Valid keys are: {self.valid_keys}")

        if (dataset_id := kwargs.get("dataset_id")) is not None:
            self["dataset_id"] = dataset_id
        if (subject_id := kwargs.get("subject_id")) is not None:
            self["subject_id"] = subject_id

        if (subject_id_source := kwargs.get("subject_id_source")) is not None:
            self["subject_id_source"] = subject_id_source

        if (age := kwargs.get("age")) is not None:
            self["age"] = age
        if (gender := kwargs.get("gender")) is not None:
            self["gender"] = gender

        if (imaging_modality := kwargs.get("imaging_modality")) is not None:
            self["imaging_modality"] = imaging_modality
        if (imaging_manufacturer := kwargs.get("imaging_manufacturer")) is not None:
            self["imaging_manufacturer"] = imaging_manufacturer
        if (subject_orientation := kwargs.get("subject_orientation")) is not None:
            self["subject_orientation"] = subject_orientation

    def __setitem__(self, key, value):
        if key not in self.valid_keys:
            raise KeyError(f"Invalid key: {key}. Valid keys are: {self.valid_keys}")
        super().__setitem__(key, value)

    @check_valid_label_value
    def set_segmentation_value(self, label: BoneLabelMap, value: int):
        if "segmentation" not in self:
            self["segmentation"] = {}
        self["segmentation"][label.name] = value

    @check_valid_label_value
    def set_mesh_value(self, label: BoneLabelMap, value: int):
        if "mesh" not in self:
            self["mesh"] = {}
        self["mesh"][label.name] = value

    @check_valid_label_value
    def set_nurbs_value(self, label: BoneLabelMap, value: int):
        if "nurbs" not in self:
            self["nurbs"] = {}
        self["nurbs"][label.name] = value
