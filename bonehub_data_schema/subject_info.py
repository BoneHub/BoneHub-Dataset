from pydantic import BaseModel, Field, field_validator, ConfigDict
from . import BoneLabelMap

_BONE_LABELS = BoneLabelMap.get_names_list()


def check_valid_label_value(label, value) -> None:
    if label not in _BONE_LABELS:
        raise ValueError(f"Invalid label: {label}. Refer to 'bonehub_data_schema/labelmap.py' to see valid labels.")
    if value not in {-1, 0, 1, 2}:
        raise ValueError(
            f"Invalid value: {value}. Valid values are: -1 (not available - not sure), 0 (not available - for sure), 1 (available - from original source), 2 (available - generated by BoneHub)"
        )


class SubjectInfo(BaseModel):
    """Data model for a subject with automatic validation."""

    dataset_id: int | None = Field(None, description="Unique identifier for the dataset in BoneHub")
    subject_id: int | None = Field(None, description="Unique identifier for the subject within the dataset")
    source_subject_path: str | None = Field(None, description="Path to the subject in the source dataset")
    age: int | None = Field(None, description="Age of the subject")
    gender: str | None = Field(None, description="Gender of the subject (M, F, O, or empty)")
    weight: float | None = Field(None, description="Weight of the subject in kg")
    height: float | None = Field(None, description="Height of the subject in cm")
    bmi: float | None = Field(None, description="Body Mass Index of the subject")
    vital_status: str | None = Field(None, description="Vital status of the subject (e.g., alive, deceased)")
    imaging_modality: str | None = Field(None, description="Imaging modality used (e.g., CT, MRI)")
    image: bool = Field(..., description="Availability value for the image (`true` or `false`)")
    segmentation: dict | None = Field(None, description="Dictionary mapping LabelMap labels to availability values")
    mesh: dict | None = Field(None, description="Dictionary mapping LabelMap labels to availability values")
    nurbs: dict | None = Field(None, description="Dictionary mapping LabelMap labels to availability values")
    remarks: str | None = Field(None, description="Any additional remarks or notes about the subject")

    model_config = ConfigDict(validate_assignment=True, strict=True)

    @field_validator("gender")
    @classmethod
    def check_gender(cls, value):
        if value in {None, "", "M", "F", "O"}:
            return value
        raise ValueError("Invalid gender. Valid values are: 'M', 'F', 'O', or empty.")

    @field_validator("segmentation", "mesh", "nurbs")
    @classmethod
    def check_label_dict(cls, v):
        """Validate that the keys in the segmentation, mesh, and nurbs dictionaries are valid labels."""
        if v is not None:
            for label, val in v.items():
                check_valid_label_value(label, val)
        return v

    def sorted_dict(self) -> dict:
        """Return the subject info sorted by field definition order, excluding None values."""
        sorted_segmentation = {
            key: self.segmentation[key] for key in _BONE_LABELS if self.segmentation and key in self.segmentation
        }
        sorted_mesh = {key: self.mesh[key] for key in _BONE_LABELS if self.mesh and key in self.mesh}
        sorted_nurbs = {key: self.nurbs[key] for key in _BONE_LABELS if self.nurbs and key in self.nurbs}
        sorted_dict = {key: getattr(self, key) for key in self.__class__.model_fields if getattr(self, key) is not None}
        if sorted_segmentation:
            sorted_dict["segmentation"] = sorted_segmentation
        if sorted_mesh:
            sorted_dict["mesh"] = sorted_mesh
        if sorted_nurbs:
            sorted_dict["nurbs"] = sorted_nurbs
        return sorted_dict

    def set_segmentation_value(self, label: str, value: int):
        """Set segmentation availability value for a label."""
        check_valid_label_value(label, value)
        if self.segmentation is None:
            object.__setattr__(self, "segmentation", {})
        self.segmentation[label] = value

    def set_mesh_value(self, label: str, value: int):
        """Set mesh availability value for a label."""
        check_valid_label_value(label, value)
        if self.mesh is None:
            object.__setattr__(self, "mesh", {})
        self.mesh[label] = value

    def set_nurbs_value(self, label: str, value: int):
        """Set NURBS availability value for a label."""
        check_valid_label_value(label, value)
        if self.nurbs is None:
            object.__setattr__(self, "nurbs", {})
        self.nurbs[label] = value
